# Reading: if you try to open a non-existent file for reading 'r': you get an error
# Reading: if you try to open an existing file for reading 'r': reads the file

# Writing: if you try to open a non-existent file for writing 'w': it will create a new file
# Writing: if you try to open an existing file from writing 'w': overwrites the contents
# Writing: if you try to open an existing file from APPENDING 'a': appends the file (adds to the end)

# File opening modes:
# 'r': read-only
# 'w': write-only
# 'a': write-only at the end of the file






